// Task 1: Declare and assign a value to the "temperature" variable
var temperature = 32;

// Task 2: Check if the temperature is greater than or equal to 30
if (temperature >= 30) {
  console.log("It's a hot day!");
} else {
  console.log("It's a normal day!");
}

// Task 3: Declare and assign a value to the "isRaining" variable
var isRaining = false;

// Task 4: Check if it's raining
if (isRaining) {
  console.log("Don't forget your umbrella!");
} else {
  console.log("Enjoy your day!");
}

// Task 5: Declare and assign a value to the "time" variable
var time = 15;

// Task 6: Check the time of the day
if (time >= 6 && time <= 12) {
  console.log("Good morning!");
} else if (time > 12 && time <= 18) {
  console.log("Good afternoon!");
} else if (time > 18 && time <= 24) {
  console.log("Good evening!");
} else {
  console.log("Good night!");
}
